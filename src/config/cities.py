def get_city_batches(batch_size: int = 50) -> list[list[City]]:
    """Split cities into batches for API requests.

    Args:
        batch_size: Number of cities per batch (default: 50).

    Returns:
        List of city batches.
    """
    return [
        INDIAN_CITIES[i : i + batch_size]
        for i in range(0, len(INDIAN_CITIES), batch_size)
    ]


def get_city_by_name(name: str) -> City | None:
    """Look up a city by name (case-insensitive)."""
    name_lower = name.lower()
    for city in INDIAN_CITIES:
        if city.name.lower() == name_lower:
            return city
    return None


def get_cities_by_state(state: str) -> list[City]:
    """Get all cities in a given state (case-insensitive)."""
    state_lower = state.lower()
    return [c for c in INDIAN_CITIES if c.state.lower() == state_lower]
